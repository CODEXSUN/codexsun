function toStorefrontProduct(product: Product): StorefrontProduct {
  const prices = product.prices.filter((price) => price.isActive)
  const reviews = product.reviews.filter((review) => review.isActive)
  const variantAttributes = product.variants.flatMap((variant) => variant.attributes)
  const colors = uniqueStrings(
    variantAttributes
      .filter((attribute) => attribute.attributeName.toLowerCase() === "color")
      .map((attribute) => attribute.attributeValue)
  ).map((name) => ({
    name,
    swatch:
      {
        Indigo: "#29446a",
        Ivory: "#efe6d7",
        Sand: "#c8a67e",
      }[name] ?? null,
  }))
  const sizes = uniqueStrings(
    variantAttributes
      .filter((attribute) => attribute.attributeName.toLowerCase() === "size")
      .map((attribute) => attribute.attributeValue)
  )
  const price = prices[0]?.sellingPrice ?? product.basePrice
  const compareAtPrice = prices[0]?.mrp ?? null
  const inventory = product.stockItems.reduce(
    (sum, item) => sum + item.quantity - item.reservedQuantity,
    0
  )

  return {
    id: product.id,
    slug: product.slug,
    sku: product.sku,
    name: product.name,
    brand: product.brandName ?? "Unbranded",
    brandSlug: slugify(product.brandName ?? "brand"),
    categorySlug: slugify(product.categoryName ?? "catalog"),
    categoryName: product.categoryName ?? "Catalog",
    department: product.storefront?.department ?? product.storefrontDepartment ?? "women",
    price,
    compareAtPrice,
    rating: averageRating(reviews.map((review) => review.rating)),
    reviewCount: reviews.length,
    inventory,
    homeSlider: product.storefront?.homeSliderEnabled ?? false,
    homeSliderOrder: product.storefront?.homeSliderOrder ?? 0,
    promoSlider: product.storefront?.promoSliderEnabled ?? false,
    promoSliderOrder: product.storefront?.promoSliderOrder ?? 0,
    featureSection: product.storefront?.featureSectionEnabled ?? false,
    featureSectionOrder: product.storefront?.featureSectionOrder ?? 0,
    featured: product.isFeatured,
    newArrival: product.storefront?.isNewArrival ?? product.isNewArrival,
    bestseller: product.storefront?.isBestSeller ?? product.isBestSeller,
    featuredLabel: product.storefront?.isFeaturedLabel ?? product.isFeaturedLabel,
    catalogBadge: product.storefront?.catalogBadge ?? null,
    images: product.images
      .filter((image) => image.isActive)
      .sort((left, right) => left.sortOrder - right.sortOrder)
      .map((image) => image.imageUrl),
    colors,
    sizes,
    fabric: product.storefront?.fabric ?? null,
    fit: product.storefront?.fit ?? null,
    sleeve: product.storefront?.sleeve ?? null,
    occasion: product.storefront?.occasion ?? null,
    shortDescription: product.shortDescription,
    description: product.description,
    shippingNote: product.storefront?.shippingNote ?? null,
    createdAt: product.createdAt,
    updatedAt: product.updatedAt,
  }
}

function buildStorefrontCatalog(items: Product[]): StorefrontCatalogResponse {
  const storefrontProducts = items.map(toStorefrontProduct)

  const brands = new Map<string, StorefrontBrand>()
  const categories = new Map<string, StorefrontCategory>()
  const reviews: StorefrontReview[] = []

  for (const product of items) {
    const storefrontProduct = storefrontProducts.find((item) => item.id === product.id)
    if (!storefrontProduct) {
      continue
    }

    const brandId = product.brandId ?? `brand:${slugify(storefrontProduct.brand)}`
    const categoryId =
      product.categoryId ?? `product-category:${slugify(storefrontProduct.categoryName)}`
    const categoryKey = storefrontProduct.categorySlug as keyof typeof categoryMeta
    const categoryConfig =
      categoryMeta[categoryKey] ??
      ({
        department: storefrontProduct.department,
        description: `${storefrontProduct.categoryName} catalog segment.`,
        image:
          storefrontProduct.images[0] ??
          placeholderImage(storefrontProduct.categoryName, "efe5d8"),
        positionOrder: 99,
      } satisfies (typeof categoryMeta)[keyof typeof categoryMeta])

    if (!brands.has(brandId)) {
      brands.set(brandId, {
        id: brandId,
        name: storefrontProduct.brand,
        slug: storefrontProduct.brandSlug,
        description: `${storefrontProduct.brand} label inside the commerce workspace.`,
        image: storefrontProduct.images[0] ?? null,
        productCount: 0,
        featuredLabel: storefrontProduct.featuredLabel,
      })
    }

    const brand = brands.get(brandId)
    if (brand) {
      brand.productCount += 1
      brand.featuredLabel = brand.featuredLabel || storefrontProduct.featuredLabel
    }

    if (!categories.has(categoryId)) {
      categories.set(categoryId, {
        id: categoryId,
        name: storefrontProduct.categoryName,
        slug: storefrontProduct.categorySlug,
        department: categoryConfig.department,
        description: categoryConfig.description,
        image: categoryConfig.image,
        menuImage: categoryConfig.image,
        positionOrder: categoryConfig.positionOrder,
        showInTopMenu: true,
        showInCatalogSection: true,
        productCount: 0,
      })
    }

    const category = categories.get(categoryId)
    if (category) {
      category.productCount += 1
    }

    for (const review of product.reviews.filter((item) => item.isActive)) {
      reviews.push({
        id: review.id,
        productId: review.productId,
        username: review.userId ?? "Guest",
        rating: review.rating,
        title: review.review?.split(".")[0] ?? null,
        review: review.review,
        createdAt: review.reviewDate,
        verifiedPurchase: Boolean(review.userId),
      })
    }
  }

  return storefrontCatalogResponseSchema.parse({
    brands: [...brands.values()].sort((left, right) => left.name.localeCompare(right.name)),
    categories: [...categories.values()].sort(
      (left, right) => left.positionOrder - right.positionOrder
    ),
    products: storefrontProducts.sort((left, right) =>
      left.featureSectionOrder - right.featureSectionOrder ||
      left.name.localeCompare(right.name)
    ),
    reviews: reviews.sort(
      (left, right) =>
        new Date(right.createdAt).getTime() - new Date(left.createdAt).getTime()
    ),
  })
}

