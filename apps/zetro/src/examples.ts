/**
 * Example 10: Session Management
 */
export async function example10_sessionManagement() {
  console.log("Example 10: Session Management\n");

  const tools = createAgenticTools();

  try {
    const sessionId = `session-${Date.now()}`;
    tools.createSession(sessionId);
    console.log("Created session:", sessionId);

    const context = tools.getContext();
    console.log("Current context:", context);

    tools.setContext({
      activeFile: "src/component.tsx",
      selection: "handleClick() { ... }",
    });

    console.log("Updated context:", tools.getContext());

    const plan = await tools.plan(
      "Optimize this callback function for performance"
    );
    console.log("Plan created with context. Task ID:", plan.taskId);
  } catch (error) {
    console.error("Error:", error);
  }
}

/**
 * Run a specific example
 */
export async function runExample(exampleNumber: number) {
  const examples: Record<number, () => Promise<void>> = {
    1: example1_simpleChat,
    2: example2_streamingChat,
    3: example3_planning,
    4: example4_writeCode,
    5: example5_quickWorkflow,
    6: example6_fixErrors,
    7: example7_streamingPlan,
    8: example8_fullWorkflow,
    9: example9_healthCheck,
    10: example10_sessionManagement,
  };

  const example = examples[exampleNumber];
  if (!example) {
    console.error(`Example ${exampleNumber} not found`);
    return;
  }

  try {
    await example();
  } catch (error) {
    console.error(`Example ${exampleNumber} failed:`, error);
  }
}

/**
 * Run all examples (with delays between them)
 */
export async function runAllExamples() {
  const delay = (ms: number) => new Promise((resolve) => setTimeout(resolve, ms));

  for (let index = 1; index <= 10; index++) {
    console.log("\n" + "=".repeat(60));
    await runExample(index);
    if (index < 10) {
      console.log("\nWaiting before next example...");
      await delay(2000);
    }
  }
}

const isMainModule =
  process.argv[1] != null &&
  import.meta.url === new URL(`file://${process.argv[1].replace(/\\/g, "/")}`).href;

if (isMainModule) {
  const exampleNum = parseInt(process.argv[2] || "1", 10);
  runExample(exampleNum).catch(console.error);
}
