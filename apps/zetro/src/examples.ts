/**
 * Example 8: Full Workflow with Context Management
 */
export async function example8_fullWorkflow() {
  console.log("Example 8: Full Workflow with Context\n");

  const tools = createAgenticTools(undefined, {
    projectId: "default",
    sessionId: "example-8",
  });

  try {
    console.log("Phase 1: Planning...");
    const plan = await tools.plan(
      "Create a new authentication middleware for Express"
    );
    console.log("Plan complete. Task ID:", plan.taskId);
    console.log("  Steps:", plan.plan.steps.length);

    console.log("\nPhase 2: Writing...");
    tools.setContext({
      activeFile: "src/middleware/auth.ts",
    });
    const write = await tools.write(
      "Implement the authentication middleware based on the plan"
    );
    console.log("Code written. Task ID:", write.taskId);

    console.log("\nPhase 3: Executing...");
    const execute = await tools.execute(
      "Apply the authentication middleware changes to the codebase"
    );
    console.log("Changes applied. Task ID:", execute.taskId);
    console.log("  Files affected:", execute.discoveredFiles.length);

    console.log("\nWorkflow complete!");
  } catch (error) {
    console.error("Error:", error);
  }
}

